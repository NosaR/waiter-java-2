package com.example.restaurant4;

public class Data_2 {
    private String id, nim, nama, alamat, telpon, total, l_id, l_nim, l_nama;

    public Data_2() {
    }

    public Data_2(String id, String nim, String nama, String alamat, String telpon, String total, String l_id, String l_nim, String l_nama) {
        this.id = id;
        this.nim = nim;
        this.nama = nama;
        this.alamat = alamat;
        this.telpon = telpon;
        this.total = total;
        this.l_id = l_id;
        this.l_nim = l_nim;
        this.l_nama = l_nama;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTelpon() {
        return telpon;
    }

    public void setTelpon(String telpon) {
        this.telpon = telpon;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getL_id() {
        return l_id;
    }

    public void setL_id(String l_id) {
        this.l_id = l_id;
    }

    public String getL_nim() {
        return l_nim;
    }

    public void setL_nim(String l_nim) {
        this.l_nim = l_nim;
    }

    public String getL_nama() {
        return l_nama;
    }

    public void setL_nama(String l_nama) {
        this.l_nama = l_nama;
    }
}
